package chapters.chapter04.Checkpoint04;

public class Checkpoint_04_15 {

	public static void main(String[] args) {

		System.out.println('a' < 'b');     //t
		System.out.println('a' <= 'A');    //f
		System.out.println('a' > 'b');     //t
		System.out.println('a' >= 'A');    //t
		System.out.println('a' == 'a');    //t
		System.out.println('a' != 'b');	   //t

	}

}
