package chapters.chapter04.Checkpoint04;

public class Checkpoint_04_08 {

	public static void main(String[] args) {

		char ch1 = '1';
		char ch2 = 'A';
		char ch3 = 'B';
		char ch4 = 'a';
		char ch5 = 'b';
		System.out.println((int) ch1);
		System.out.println((int) ch2);
		System.out.println((int) ch3);
		System.out.println((int) ch4);
		System.out.println((int) ch5);
		System.out.println("------------------------------------------------------------");

		char ch11 = (char) 40;
		char ch22 = (char) 59;
		char ch33 = (char) 79;
		char ch44 = (char) 85;
		char ch55 = (char) 90;

		System.out.println(ch11);
		System.out.println(ch22);
		System.out.println(ch33);
		System.out.println(ch44);
		System.out.println(ch55);
		System.out.println("-----------------------------------------------------------------");

		System.out.println((char) 0X40);
		System.out.println((char) 0X5A);
		System.out.println((char) 0X71);
		System.out.println((char) 0X72);
		System.out.println((char) 0X7a);

	}
}
