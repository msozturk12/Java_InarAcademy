package chapters.chapter03.Checkpoint03;

public class Checkpoint_03_09 {
	public static void main(String[] args) {

		int x = 2;
		int y = 3;
		int z;

		if (x > 2) {
			if (y > 2) {
				z = x + y;
				System.out.println("z is " + z);
			}
		} else
			System.out.println("x is " + x);

	}
}
//output is:  x is 2
