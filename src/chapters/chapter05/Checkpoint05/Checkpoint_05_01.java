package chapters.chapter05.Checkpoint05;

public class Checkpoint_05_01 {
	public static void main(String[] args) {
		
		
		int count = 0;
		while (count < 100) {
		// Point A                               count < 100 always true in that point
		System.out.println("Welcome to Java!");
		count++;
		// Point B								count < 100 sometimes true sometimes false in that point
		}
		// Point C								count < 100 always false in that point
		
		
		
	}

}
